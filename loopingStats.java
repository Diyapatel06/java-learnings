class forloop{
    public static void main(String[] args){
        for(int i=1;i<=5;i++){
            System.out.print( i );
        }
    }
}

class evenNum{
    public static void main(String[] args){
        for(int i=0;i<=50;i=i+2){
            System.out.println(i);
        }
    }
}

class dowhileExamp{
    public static void main(String[] args){
        int i=1;

        do{
            System.out.println(i);
            i++;
        }while(i<=5);
    }
}

